package Demo1;

public class OperatorDemo {
    public static void main(String[] args) {
        int a = 3;
        int b = 4;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a / 4.0);
    }

}
